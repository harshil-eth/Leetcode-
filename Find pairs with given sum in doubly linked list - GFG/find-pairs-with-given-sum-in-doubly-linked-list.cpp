class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int t)
    {
        // code here
        vector<pair<int, int>> ans;
        Node* curr = head;
        Node* curr2 = head;
        while(curr2->next != NULL) {
            curr2 = curr2->next;
        }
        
        while(curr->data < curr2->data) {
            if(curr->data + curr2->data < t) {
                curr=curr->next;
            }
            else if(curr->data + curr2->data > t) {
                curr2=curr2->prev;
            }
            else {
                ans.push_back({curr->data, curr2->data});
                curr=curr->next;
                curr2=curr2->prev;
            }
        }
        
        return ans;
    }
};

